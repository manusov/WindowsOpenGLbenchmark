int FontLoader::init(const void* rawData)
{
	constexpr int FONT_LINE_SIZE = APPCONST::TEXTURE_WIDTH * 16;
	DWORD32* ptrUnpackedFont = (DWORD32*)(rawData);
	for (int i = 0; i < (FONT_LINE_SIZE * 2); i++)
	{
		ptrUnpackedFont[i] = APPCONST::TEXT_BACK_COLOR;
	}
	
	ptrUnpackedFont = (DWORD32*)(rawData);
	DWORD32* temp1 = nullptr;
	DWORD32* temp2 = nullptr;

	for (int i = 0; i < 2; i++)                     // Cycle for 2 fonts.
	{
		const BYTE* ptrRasterFont = &rasterFont[0];
		temp1 = ptrUnpackedFont;
		for (int j = 0; j < 128; j++)               // Cycle for 128 chars per font.
		{
			temp2 = ptrUnpackedFont;
			ptrUnpackedFont += APPCONST::TEXTURE_WIDTH * 15;
			for (int k = 0; k < 16; k++)            // Cycle for 16 lines per font.
			{
				BYTE bitMap = *(ptrRasterFont++);
				BYTE bitMask = 0x80;
				DWORD32 color;
				for (int n = 0; n < 8; n++)         // Cycle for 8 pixels per one font line.
				{
					if (!(bitMap & bitMask))
					{
						color = APPCONST::TEXT_BACK_COLOR;
					}
					else if (!i)
					{
						color = APPCONST::TEXT_FRONT_COLOR_1;
					}
					else
					{
						color = APPCONST::TEXT_FRONT_COLOR_2;
					}
					*(ptrUnpackedFont++) = color;
					bitMask >>= 1;
				}
				ptrUnpackedFont -= (APPCONST::TEXTURE_WIDTH + 8);
			}
			ptrUnpackedFont = temp2 + 8;
		}
		ptrUnpackedFont = temp1 + APPCONST::TEXTURE_WIDTH * 16;
	}

	return 0;
}

